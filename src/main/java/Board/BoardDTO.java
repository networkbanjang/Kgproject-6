package Board;

public class BoardDTO {
	int num;
	String id;
	String content;
	String title;
	String photo;
	String category;
	String minor_v;
	String minor_an;
	String nick;
	String time;
	int point;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public String getMinor_v() {
		return minor_v;
	}
	public void setMinor_v(String minor_v) {
		this.minor_v = minor_v;
	}
	public String getMinor_an() {
		return minor_an;
	}
	public void setMinor_an(String minor_an) {
		this.minor_an = minor_an;
	}

	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}

}
